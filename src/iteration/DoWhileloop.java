package iteration;

public class DoWhileloop {

	public static void main(String[] args) {
		
		
		// do-while evaluates its expression at the bottom of the loop instead of the top. 
		
		int count = 1;
        do {
            System.out.println("Count is: " + count);
            count++;
        } while (count < 11);
        
        
        
        

	}

}
